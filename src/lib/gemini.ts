// Targeted Gap Repair — per-division focused prompts
// ═══════════════════════════════════════════════════════════════════════════════

export interface RepairInput {
  divisions: any[];
  subjects: any[];
  teachers: any[];
  days: number;
  slotsPerDay: number;
  morningPeriods: number;
  divisionExclusions?: { divisionId: string; divisionName: string; subjectId: string; subjectName: string }[];
  existingTimetable: any[];
}

export async function repairTimetableGaps(
  input: RepairInput,
  activeConstraints: string[],
): Promise<any[] | null> {
  const { divisions, subjects, teachers, days, slotsPerDay, morningPeriods, existingTimetable } = input;
  const exclusionSet = new Set(
    (input.divisionExclusions ?? []).map(e => `${e.divisionId}:${e.subjectId}`)
  );

  // Build teacher occupancy from existing timetable
  const teacherOccupied = new Set<string>();
  const divOccupied = new Set<string>();
  for (const entry of existingTimetable) {
    teacherOccupied.add(`${entry.teacherId}:${entry.dayOfWeek}:${entry.slotNumber}`);
    divOccupied.add(`${entry.divisionId}:${entry.dayOfWeek}:${entry.slotNumber}`);
  }

  // Find gaps per division
  const gapsByDivision = new Map<string, { day: number; slot: number }[]>();
  for (const div of divisions) {
    const gaps: { day: number; slot: number }[] = [];
    for (let d = 1; d <= days; d++) {
      for (let s = 1; s <= slotsPerDay; s++) {
        if (!divOccupied.has(`${div.id}:${d}:${s}`)) {
          gaps.push({ day: d, slot: s });
        }
      }
    }
    if (gaps.length > 0) {
      gapsByDivision.set(div.id, gaps);
    }
  }

  if (gapsByDivision.size === 0) return null;

  // Find which subjects are under-scheduled per division
  const allRepairs: any[] = [];

  for (const [divId, gaps] of gapsByDivision) {
    const div = divisions.find((d: any) => d.id === divId);
    if (!div) continue;

    // Calculate which subjects still need periods
    const subjectNeeds: { subjectId: string; subjectName: string; teacherId: string; needed: number }[] = [];
    for (const ds of div.subjects) {
      const placed = existingTimetable.filter(
        (e: any) => e.divisionId === divId && e.subjectId === ds.subjectId
      ).length;
      const needed = ds.periodsPerWeek - placed;
      if (needed > 0 && !exclusionSet.has(`${divId}:${ds.subjectId}`)) {
        const sub = subjects.find((s: any) => s.id === ds.subjectId);
        subjectNeeds.push({
          subjectId: ds.subjectId,
          subjectName: sub?.name ?? ds.subjectId,
          teacherId: ds.teacherId,
          needed,
        });
      }
    }

    if (subjectNeeds.length === 0) continue;

    // Build teacher availability for this division's teachers
    const relevantTeacherIds = new Set(subjectNeeds.map(s => s.teacherId));
    const teacherAvailability: Record<string, { day: number; slot: number }[]> = {};
    for (const tid of relevantTeacherIds) {
      const available: { day: number; slot: number }[] = [];
      for (const gap of gaps) {
        if (!teacherOccupied.has(`${tid}:${gap.day}:${gap.slot}`)) {
          available.push(gap);
        }
      }
      const tName = teachers.find((t: any) => t.id === tid)?.name ?? tid;
      teacherAvailability[`${tid} (${tName})`] = available;
    }

    const prompt = `You are filling GAPS in a school timetable for division "${div.name}".

### Empty Slots to Fill
${JSON.stringify(gaps)}

### Subjects That Need More Periods
${JSON.stringify(subjectNeeds, null, 2)}

### Teacher Availability (only at times when they are NOT teaching elsewhere)
${JSON.stringify(teacherAvailability, null, 2)}

### Rules
- Each gap slot can only have ONE subject
- A teacher can only be in ONE place at a time (respect the availability above)
- Try to avoid placing the same subject twice on the same day
- Use ONLY the exact subjectId and teacherId values from the data above

Return a JSON array of objects to fill the gaps:
[{"divisionId":"${divId}","dayOfWeek":N,"slotNumber":N,"subjectId":"...","teacherId":"..."}]

Fill as many gaps as possible. Return ONLY the JSON array, no explanation.`;

    try {
      const response = await ai.models.generateContent({
        model: 'gemini-3.5-flash',
        contents: prompt,
      });
      const text = (response.text || '').trim();
      const jsonMatch = text.match(/\[[\s\S]*\]/);
      if (jsonMatch) {
        const parsed = JSON.parse(jsonMatch[0]);
        if (Array.isArray(parsed)) {
          allRepairs.push(...parsed);
        }
      }
    } catch (error) {
      console.error(`Gemini gap repair error for division ${div.name}:`, error);
      // Continue with other divisions
    }
  }

  return allRepairs.length > 0 ? allRepairs : null;
}
